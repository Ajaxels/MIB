%% Plugins Menu
% This menu allows access to custom plugins. 
%
% The plugins should be placed under
% |im_browser->Plugins| directory as |PLUGIN_GROUP_NAME/PLUGIN_NAME|, where PLUGIN_GROUP_NAME is a directory with similarly grouped
% plugins. PLUGIN_NAME - is a directory with the actual plugin. The PLUGIN_NAME directory should be the same as the main
% function of the plugin, for example |plugin_name.m|. So the full filename
% may look like this:
%
% _c:\Matlab\Scripts\im_browser\Plugins\MyPlugings\plugin1\plugin1.m_
%
% By default there is a folder |Plugins->Tests| that has two plugins: <http://www.biocenter.helsinki.fi/~ibelev/projects/im_browser/tutorials/pluginOtsu.html otsuThresholding> 
% and <http://www.biocenter.helsinki.fi/~ibelev/projects/im_browser/tutorials/pluginGui.html guiTutorial>. See more in the
% corresponding tutorials.
%
% *Back to* <im_browser_product_page.html *Index*> |*-->*| <im_browser_user_guide.html *User Guide*> |*-->*| <ug_gui_menu.html *Menu*>
%